#include <stdio.h>

void main()
{
    printf("%d + %d = %d\n", 5, 2, 5 + 2);
    printf("%d - %d = %d\n", 5, 2, 5 - 2);
    printf("%d * %d = %d\n", 5, 2, 5 * 2);
    printf("%d / %d = %d\n", 5, 2, 5 / 2);
    printf("%d %% %d = %d\n", 5, 2, 5 % 2);
}