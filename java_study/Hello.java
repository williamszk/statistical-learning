


public class Hello {
    
    public static void main(String[] args) {
        System.out.println("Hello, William!");

        // declaration vs initialization
        int myFirstNumber = 5;
        // in initialization we assign an initial value.

        System.out.println(myFirstNumber);
        // String literal

        int myFirstNumber01 = (10 + 5) * (2 + 10);
        System.out.println(myFirstNumber01);

        int mySecondNumber = 12;
        int myThirdNumber = 6;
        int myTotal = myFirstNumber01 + mySecondNumber + myThirdNumber;

        System.out.println(myTotal);

        int myLastOne = 1000 - myTotal;

        System.out.println(myLastOne);

    }
}
